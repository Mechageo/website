<input type="text" name="link" id="link" onkeypress="return runScript(event)">
<input type="submit" value="Let's Go!" onclick="goGoGadgetURL()">
<p id="test"></p>
<script>
//Set the focus when the window loads
window.onload = function() {
  document.getElementById("link").focus();
};

function goGoGadgetURL() {

var url = document.getElementById("link").value;

//If URL contains letters and not just numbers
if(/[a-z]/i.test(url))
{
	var flan = "https://epm.verisk.com/jira/browse/";
	url = url.toUpperCase();
}
else
{
	var flan = "https://epm.verisk.com/jira/browse/XMW-";
}

var destination = flan+url;

document.getElementById("test").innerHTML = destination;
window.location.href = destination;
}

//Lets you hit enter instead of clicking the button
function runScript(e) {
    if (e.keyCode == 13) {
        goGoGadgetURL();
        return false;
    }
}
</script>
